//Replace: searches for a substring and replaces it with a different text (takes two parameters)

string sayHello = "Hello World!"; 
Console.WriteLine(sayHello); 
sayHello = sayHello.Replace("Hello", "Greetings"); //Hello gets replaced by another string value(Greeting)
Console.WriteLine(sayHello); 

//A method to make a string all uppercase

Console.WriteLine(sayHello.ToUpper());

//A method to make a string all lowercase 

Console.WriteLine(sayHello.ToLower()); 